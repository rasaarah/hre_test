public class VerifyElement {
	boolean result
	
	def verifyMatch(String actual, String expected) {
		if (WebUI.verifyMatch(actual.toString().trim(), expected.toString().trim(), false, FailureHandling.OPTIONAL)) {
			result = true
			KeywordUtil.markPassed("MATCH VALUE BETWEEN " + actual + " AND " + expected)
		} else {
			result = false
			KeywordUtil.markFailed("NOT MATCH VALUE BETWEEN " + actual + " AND " + expected)
		}
		return result
	}
}
